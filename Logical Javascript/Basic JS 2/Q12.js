function func() { 
  
    // Original Array 
    var arr = ['ENTC', 'Science',  
               'Computer', 'Mechanical']; 
    document.write("List in given order:"+arr); 
  
    // Reversed array 
    var new_arr = arr.reverse(); 
    document.write("<br>"); 
    document.write("List in reverse order:"+new_arr); 
} 
func(); 